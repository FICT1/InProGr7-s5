#Ejercicio 6. Evaluar si un estudiante aprueba o reprueba
#• Ingresar una calificación.
#• Si es mayor o igual a 70, mostrar "Aprobado"; de lo contrario, "Reprobado".

nota=int(input("Ingrese la calificación del estudiante: "))
if nota>=70:
    print("Aprobado")
else:
    print("Reprobado")
